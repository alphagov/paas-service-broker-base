func TestTLSConfig_GenerateTLSConfig(t *testing.T) {

	tests := []struct {
		name        string
		certificate string
		privateKey  string
		ca          string
		wantErr     bool
	}{
		{
			name:        "valid certs work",
			certificate: rsaCertPEM,
			privateKey:  rsaKeyPEM,
			ca:          "",
			wantErr:     false,
		},
		{
			name:        "valid certs dont work",
			certificate: "",
			privateKey:  "",
			ca:          "",
			wantErr:     true,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			tr := &TLSConfig{
				Certificate: tt.certificate,
				PrivateKey:  tt.privateKey,
				CA:          tt.ca,
			}
			_, err := tr.GenerateTLSConfig()
			if (err != nil) != tt.wantErr {
				t.Errorf("TLSConfig.GenerateTLSConfig() error = %v, wantErr %v", err, tt.wantErr)
				return
			}
		})
	}
}
